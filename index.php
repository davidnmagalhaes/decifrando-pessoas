        const target = button.getAttribute('data-target');
        const div = document.getElementById(target);

        // Fecha todas as outras divs e remove a classe "active" de todos os botões
        divs.forEach(otherDiv => {
            if (otherDiv.id !== target) {
                otherDiv.style.display = 'none';
            }
        });

        buttons.forEach(otherButton => {
            otherButton.classList.remove('active');
        });

        // Abre a div alvo e adiciona a classe "active" apenas ao botão correspondente
        if (div.style.display === 'flex') {
            div.style.display = 'none';
        } else {
            div.style.display = 'flex';
            button.classList.add('active');

            // Scroll para a div alvo
            const anchor = button.getAttribute('data-anchor');
            const targetDiv = document.querySelector(anchor);
            targetDiv.scrollIntoView({ behavior: 'smooth' });
        }

        // Impede que a âncora padrão seja seguida
